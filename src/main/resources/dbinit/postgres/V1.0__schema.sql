-- apply post alter
update budget_request set sys_period = tstzrange(_created_at, null);
create table budget_request_history(like budget_request);
create view budget_request_with_history as select * from budget_request union all select * from budget_request_history;
create or replace function budget_request_history_version() returns trigger as $$
declare
  lowerTs timestamptz;
  upperTs timestamptz;
begin
  lowerTs = lower(OLD.sys_period);
  upperTs = greatest(lowerTs + '1 microsecond',current_timestamp);
  if (TG_OP = 'UPDATE') then
    insert into budget_request_history (sys_period,id, title, description, amount, currency, user_request_id, transaction_type, budget_status_id, budget_type_id, _version, _created_at, _created_by, _updated_at, _updated_by) values (tstzrange(lowerTs,upperTs), OLD.id, OLD.title, OLD.description, OLD.amount, OLD.currency, OLD.user_request_id, OLD.transaction_type, OLD.budget_status_id, OLD.budget_type_id, OLD._version, OLD._created_at, OLD._created_by, OLD._updated_at, OLD._updated_by);
    NEW.sys_period = tstzrange(upperTs,null);
    return new;
  elsif (TG_OP = 'DELETE') then
    insert into budget_request_history (sys_period,id, title, description, amount, currency, user_request_id, transaction_type, budget_status_id, budget_type_id, _version, _created_at, _created_by, _updated_at, _updated_by) values (tstzrange(lowerTs,upperTs), OLD.id, OLD.title, OLD.description, OLD.amount, OLD.currency, OLD.user_request_id, OLD.transaction_type, OLD.budget_status_id, OLD.budget_type_id, OLD._version, OLD._created_at, OLD._created_by, OLD._updated_at, OLD._updated_by);
    return old;
  end if;
end;
$$ LANGUAGE plpgsql;

create trigger budget_request_history_upd
  before update or delete on budget_request
  for each row execute procedure budget_request_history_version();


