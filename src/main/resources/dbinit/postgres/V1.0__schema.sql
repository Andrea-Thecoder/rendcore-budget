create table budget_request (
  id                            uuid not null,
  description                   TEXT not null,
  user_request_id               uuid not null,
  _version                      bigint not null,
  _created_at                   timestamptz not null,
  _updated_at                   timestamptz not null,
  amount                        decimal(12,2) not null,
  title                         varchar(255) not null,
  currency                      varchar(3) not null,
  transaction_type              varchar(7) not null,
  budget_status_id              varchar(255) not null,
  budget_type_id                varchar(255),
  _created_by                   varchar(255) not null,
  _updated_by                   varchar(255) not null,
  constraint ck_budget_request_transaction_type check ( transaction_type in ('INCOME','EXPENSE')),
  constraint pk_budget_request primary key (id)
);

create table budget_status_change (
  id                            uuid not null,
  request_id                    uuid not null,
  performed_by                  uuid not null,
  note                          TEXT,
  _version                      bigint not null,
  _created_at                   timestamptz not null,
  _updated_at                   timestamptz not null,
  old_status_id                 varchar(255) not null,
  new_status_id                 varchar(255) not null,
  _created_by                   varchar(255) not null,
  _updated_by                   varchar(255) not null,
  constraint pk_budget_status_change primary key (id)
);

create table budget_status_transition (
  id                            bigint generated by default as identity not null,
  from_status_id                varchar(255) not null,
  to_status_id                  varchar(255) not null,
  constraint pk_budget_status_transition primary key (id)
);

create table budget_status_type (
  id                            varchar(255) not null,
  description                   varchar(255) not null,
  constraint pk_budget_status_type primary key (id)
);

create table budget_type (
  id                            varchar(255) not null,
  description                   varchar(255) not null,
  constraint pk_budget_type primary key (id)
);

